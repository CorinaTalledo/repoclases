
// Crear un programa que pida ingresar una edad y devuelva el equivalente de esas edad en años perro.

let edad = prompt('Ingrese la edad', '0');
const aniosPerro = 7 

let resultado = Number(edad) * aniosPerro;

// console.log(resultado); Comprueba que este funcionando todo


alert(`Tu edad en años perro es ${resultado} años`)








